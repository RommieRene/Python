import math


a = float(input("type number for a: "))
b = float(input("type number for b: "))
print("a + b =", a + b, 'Sum of a and b')
print("a - b =", a - b, 'Difference between a and b')
print("a * b =", a * b, 'Product of a and b')
print("a / b =", a / b, 'Quotient of a and b (a divided by b)')
print("a % b =", a % b, 'Remainder of a divided by b')
print("log10(a) =", math.log10(a), 'log base 10 of a')
print("a ** b =", a ** b, 'a raised to the power of b')


